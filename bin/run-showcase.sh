#!/usr/bin/env bash
# Run the Counterfactual Court against the showcase PR
# (vitejs/vite-plugin-react#1192). Idempotent: safe to re-run.
#
# Steps (each prints intent before doing it):
#   1. Verify prerequisites (git, gh, pnpm, ollama).
#   2. Clone vitejs/vite-plugin-react at the PR base commit into .tmp/.
#   3. Fetch the PR as a unified patch via gh.
#   4. Verify the patch applies cleanly to the base.
#   5. Build the fixture under fixtures/showcase-vite-rsc/.
#   6. Build the package (pnpm build) if dist/ is missing.
#   7. Invoke gemmacourt run with all Phase 2 flags and a 50m budget,
#      writing the bundle into bundles/showcase/.
#   8. Print the resulting bundle path so the caller can pipe it into
#      pnpm gemmacourt verify and replay.
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TMP_DIR="${REPO_ROOT}/.tmp/showcase"
TARGET_DIR="${TMP_DIR}/vite-plugin-react"
FIXTURE_NAME="showcase-vite-rsc"
FIXTURE_DIR="${REPO_ROOT}/fixtures/${FIXTURE_NAME}"
BUNDLES_DIR="${REPO_ROOT}/bundles/showcase"

PR_REPO="vitejs/vite-plugin-react"
PR_NUMBER="1192"
PR_BASE_SHA="323ccd72576be636b50baa7d9ce816cc94d5991e"
PR_HTML_URL="https://github.com/vitejs/vite-plugin-react/pull/${PR_NUMBER}"
PATCH_FILE_A="packages/plugin-rsc/src/plugin.ts"
PATCH_FILE_B="packages/plugin-rsc/e2e/css-code-split.test.ts"

step() {
  printf '\n[showcase] %s\n' "$1"
}

require_tool() {
  if ! command -v "$1" >/dev/null 2>&1; then
    printf 'showcase: required tool "%s" not found on PATH; install it before re-running\n' "$1" >&2
    exit 2
  fi
}

step "1/8 verify prerequisites"
require_tool git
require_tool gh
require_tool pnpm
require_tool ollama

step "2/8 clone ${PR_REPO} at base ${PR_BASE_SHA:0:12} into ${TMP_DIR}"
mkdir -p "${TMP_DIR}"
if [ ! -d "${TARGET_DIR}/.git" ]; then
  git clone --quiet "https://github.com/${PR_REPO}.git" "${TARGET_DIR}"
else
  printf '[showcase] reusing existing clone at %s\n' "${TARGET_DIR}"
  git -C "${TARGET_DIR}" fetch --quiet origin
fi
git -C "${TARGET_DIR}" checkout --quiet --detach "${PR_BASE_SHA}"

step "3/8 fetch PR #${PR_NUMBER} as a unified patch via gh"
PATCH_FILE="${TMP_DIR}/pr-${PR_NUMBER}.patch"
gh pr diff "${PR_NUMBER}" --repo "${PR_REPO}" --patch >"${PATCH_FILE}"
if [ ! -s "${PATCH_FILE}" ]; then
  printf 'showcase: gh returned an empty patch for PR %s; check auth and rate limits\n' "${PR_NUMBER}" >&2
  exit 3
fi

step "4/8 verify the patch applies cleanly against base ${PR_BASE_SHA:0:12}"
if ! git -C "${TARGET_DIR}" apply --check "${PATCH_FILE}"; then
  printf 'showcase: patch failed --check; the upstream base may have moved or the PR was rebased\n' >&2
  printf 'showcase: re-resolve PR_BASE_SHA against the current head of %s\n' "${PR_REPO}" >&2
  exit 4
fi

step "5/8 build fixture at ${FIXTURE_DIR}"
rm -rf "${FIXTURE_DIR}"
mkdir -p "${FIXTURE_DIR}/src"
cp "${PATCH_FILE}" "${FIXTURE_DIR}/patch.diff"

# repo-snippet.ts and repo-head.txt are the unpatched plugin.ts content.
# The orchestrator feeds repo-snippet.ts to the Prosecutor and Defender,
# and repo-head.txt to the Jury.
PLUGIN_TS_PATH="${TARGET_DIR}/${PATCH_FILE_A}"
if [ ! -f "${PLUGIN_TS_PATH}" ]; then
  printf 'showcase: expected file %s not found in clone\n' "${PATCH_FILE_A}" >&2
  exit 5
fi
cp "${PLUGIN_TS_PATH}" "${FIXTURE_DIR}/repo-snippet.ts"
cp "${PLUGIN_TS_PATH}" "${FIXTURE_DIR}/repo-head.txt"

# Mirror the plugin-rsc src/ tree so monorepo impact tracing has siblings
# to walk. Tests under e2e/ are not copied; they would inflate ripple noise.
PLUGIN_RSC_SRC="${TARGET_DIR}/packages/plugin-rsc/src"
if [ -d "${PLUGIN_RSC_SRC}" ]; then
  find "${PLUGIN_RSC_SRC}" -type f \( -name '*.ts' -o -name '*.tsx' \) -print0 |
    while IFS= read -r -d '' file; do
      rel="${file#"${PLUGIN_RSC_SRC}/"}"
      mkdir -p "${FIXTURE_DIR}/src/$(dirname "${rel}")"
      cp "${file}" "${FIXTURE_DIR}/src/${rel}"
    done
else
  printf 'showcase: %s missing; monorepo trace will be empty\n' "${PLUGIN_RSC_SRC}" >&2
fi

cat >"${FIXTURE_DIR}/metadata.json" <<META
{
  "name": "${FIXTURE_NAME}",
  "source": "${PR_HTML_URL}",
  "baseSha": "${PR_BASE_SHA}",
  "files": ["${PATCH_FILE_A}", "${PATCH_FILE_B}"],
  "description": "Phase 3A showcase fixture; generated by bin/run-showcase.sh from vitejs/vite-plugin-react#${PR_NUMBER}."
}
META

cat >"${FIXTURE_DIR}/pr-description.md" <<DESC
# vitejs/vite-plugin-react#${PR_NUMBER}

Source: ${PR_HTML_URL}

Title: fix(rsc): include bundled server CSS when cssCodeSplit is false

Files changed:
- ${PATCH_FILE_A}
- ${PATCH_FILE_B}

Base commit: ${PR_BASE_SHA}
DESC

step "6/8 ensure dist/ is built (pnpm build)"
if [ ! -f "${REPO_ROOT}/dist/cli/gemmacourt.js" ]; then
  ( cd "${REPO_ROOT}" && pnpm build )
else
  printf '[showcase] dist/cli/gemmacourt.js already exists, skipping pnpm build\n'
fi

step "7/8 run gemmacourt against the fixture (writes to ${BUNDLES_DIR})"
mkdir -p "${BUNDLES_DIR}"
# Bundle filenames are sha256 hex strings; ls is safe here.
# shellcheck disable=SC2012
EXISTING_BUNDLE="$(ls -1 "${BUNDLES_DIR}"/*.verdict 2>/dev/null | head -1 || true)"
if [ -n "${EXISTING_BUNDLE}" ]; then
  printf '[showcase] existing showcase bundle present: %s\n' "${EXISTING_BUNDLE}"
  printf '[showcase] re-running anyway; the bandit and Jury are deterministic, so the new bundle id should match.\n'
fi

GEMMACOURT_BUNDLES_DIR="${BUNDLES_DIR}" \
  node "${REPO_ROOT}/dist/cli/gemmacourt.js" run \
    --fixture "${FIXTURE_NAME}" \
    --evidence-graph \
    --precedent \
    --impact \
    --budget 50m

step "8/8 done"
# Bundle filenames are sha256 hex strings; ls is safe here.
# shellcheck disable=SC2012
LATEST="$(ls -t "${BUNDLES_DIR}"/*.verdict 2>/dev/null | head -1 || true)"
if [ -n "${LATEST}" ]; then
  printf '[showcase] latest bundle: %s\n' "${LATEST}"
  printf '[showcase] verify: pnpm gemmacourt verify "%s"\n' "${LATEST}"
  printf '[showcase] replay: pnpm gemmacourt replay "%s"\n' "${LATEST}"
else
  printf '[showcase] no bundle written; check the run output above\n' >&2
  exit 6
fi
